public class PhraseString {

    private static List<List> res = new ArrayList<>();

    public static void main(String[] args) {
        String s = "aab";
        partition(s);

        for (List item : res) {
            System.out.println(item);
        }
    }

    private static List<List> partition(String s) {
        if (s == null || s.length() == 0) {
            return res;
        }

        dfs(s, new ArrayList<>(), 0);
        return res;
    }

    private static void dfs(String s, List remain, int left) {
        if (left == s.length()) {  //ç»ˆæ­¢æ¡ä»¶
            res.add(new ArrayList(remain));
            return;
        }

        for (int right = left; right < s.length(); right++) { //ä»leftå¼€å§‹ï¼Œä¾æ¬¡åˆ¤æ–­left->rightæ˜¯ä¸æ˜¯å›æ–‡ä¸²
            if (isPalindroom(s, left, right)) {
                remain.add(s.substring(left, right + 1));  //æ·»åŠ åˆ°å½“å‰å›æ–‡ä¸²åˆ°listä¸­
                dfs(s, remain, right + 1);   //ä»right+1å¼€å§‹ç»§ç»­é€’å½’ï¼Œå¯»æ‰¾å›æ–‡ä¸²
                remain.remove(remain.size() - 1);   //å›æº¯ï¼Œä»è€Œå¯»æ‰¾æ›´é•¿çš„å›æ–‡ä¸²
            }
        }
    }

    /**
     * åˆ¤æ–­æ˜¯å¦ä¼šé—®ä¸²
     */
    private static boolean isPalindroom(String s, int left, int right) {
        while (left < right && s.charAt(left) == s.charAt(right)) {
            left++;
            right--;
        }

        return left >= right;
    }





















}
